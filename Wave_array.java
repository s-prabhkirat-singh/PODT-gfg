class Solution {
    public static void convertToWave(int n, int[] a) {
        // code here
        for(int i=0;i<n;i=i+2){
            if(i>n-2){break;}
            int temp=a[i+1];
            a[i+1]=a[i];
            a[i]=temp;
        }
    }
}
        
